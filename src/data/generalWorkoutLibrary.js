function exerciseItem({
  name,
  sets = null,
  reps = null,
  loadHint = null,
  effort = null,
  duration = null,
  distance = null,
  paceTarget = null,
  notes = null,
}) {
  return {
    name,
    sets,
    reps,
    loadHint,
    effort,
    duration,
    distance,
    paceTarget,
    notes,
  };
}

function block({ id, type, title, format = null, duration = null, repeat = null, notes = [], exercises = [] }) {
  return {
    id,
    type,
    title,
    format,
    duration,
    repeat,
    notes,
    exercises,
  };
}

